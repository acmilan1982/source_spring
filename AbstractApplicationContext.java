	/**
        Instantiate and invoke all registered BeanFactoryPostProcessor beans, respecting explicit order if given.
        Must be called before singleton instantiation.

		实例化，并执行所有的 BeanFactoryPostProcessor
	 */
    // 代码25
	protected void invokeBeanFactoryPostProcessors(ConfigurableListableBeanFactory beanFactory) {
       
		PostProcessorRegistrationDelegate.invokeBeanFactoryPostProcessors(beanFactory,                       // invokeBeanFactoryPostProcessors 见 PostProcessorRegistrationDelegate代码1
		                                                                  getBeanFactoryPostProcessors());   // getBeanFactoryPostProcessors获取所有BeanFactoryPostProcessor，见代码26
																		                                     // 通常，此时尚未注册任何 BeanFactoryPostProcessor
 		                                                                    

		// Detect a LoadTimeWeaver and prepare for weaving, if found in the meantime
		// (e.g. through an @Bean method registered by ConfigurationClassPostProcessor)
		if (!NativeDetector.inNativeImage() && beanFactory.getTempClassLoader() == null && beanFactory.containsBean(LOAD_TIME_WEAVER_BEAN_NAME)) {
			beanFactory.addBeanPostProcessor(new LoadTimeWeaverAwareProcessor(beanFactory));
			beanFactory.setTempClassLoader(new ContextTypeMatchClassLoader(beanFactory.getBeanClassLoader()));
		}
	}


