    /**
     * 
        Description copied from interface: ConfigurableApplicationContext
        Load or refresh the persistent representation of the configuration, which might be from Java-based configuration, an XML file, a properties file, a relational database schema, or some other format.
        As this is a startup method, it should destroy already created singletons if it fails, to avoid dangling resources. 
        In other words, after invocation of this method, either all or no singletons at all should be instantiated.

		当前方法执行之后，所有的单例对象应该都被实例化了
        Specified by:
          refresh in interface ConfigurableApplicationContext
        Throws:
          org.springframework.beans.BeansException - if the bean factory could not be initialized
          IllegalStateException  - if already initialized and multiple refresh attempts are not supported
     */
	//代码10
	@Override
	public void refresh() throws BeansException, IllegalStateException {
		synchronized (this.startupShutdownMonitor) {
			StartupStep contextRefresh = this.applicationStartup.start("spring.context.refresh");

			// 对于AnnotationConfigApplicationContext，基本上什么都不干
			// Prepare this context for refreshing.
			prepareRefresh();                                                             // 见子类 AnnotationConfigServletWebServerApplicationContext 代码3

			// 对于AnnotationConfigApplicationContext，获取 BeanFactory 实现类 ：DefaultListableBeanFactory

			// 对于 AnnotationConfigServletWebServerApplicationContext，已经在构造方法中被创建，实现类 ：DefaultListableBeanFactory
			// Tell the subclass to refresh the internal bean factory. 
			ConfigurableListableBeanFactory beanFactory = obtainFreshBeanFactory();       // 见代码15

			/*
			 * 
			 *   2. 向 beanFactory 中注册系统属性，环境变量等，以bean的方式, 具体实现为 Map
			 */
			// Prepare the bean factory for use in this context.
			prepareBeanFactory(beanFactory);                                              // 见代码20

			try {

                /**
                 * 
                    Modify the application context's internal bean factory after its standard initialization. 
                    All bean definitions will have been loaded, but no beans will have been instantiated yet. 
                    This allows for registering special BeanPostProcessors etc in certain ApplicationContext implementations.
                    Params:
                      beanFactory – the bean factory used by the application context
                 */


				// 对于AnnotationConfigApplicationContext，基本上什么都不干
				// 对于 AnnotationConfigServletWebServerApplicationContext，注册 annotatedClasses，仅注册配置类本身，未读取配置类内容
				// Allows post-processing of the bean factory in context subclasses.
				postProcessBeanFactory(beanFactory);             // 由子类实现，见 AnnotationConfigServletWebServerApplicationContext 代码10



				StartupStep beanPostProcess = this.applicationStartup.start("spring.context.beans.post-process");


               // 重要：从容器中获取所有 BeanFactoryPostProcessor，根据指定顺序，执行所有BeanFactoryPostProcessor
			   // 注意，有一些 BeanFactoryPostProcessor，此时以普通bean的方式存在，当前方法根据类型获取所有BeanFactoryPostProcessor并执行
			   /*
				*  step1: 执行所有的 BeanDefinitionRegistryPostProcessor
				*         1. 执行所有实现了 PriorityOrdered 的 BeanDefinitionRegistryPostProcessor 的 postProcessBeanDefinitionRegistry 方法
				*         2. 执行所有实现了 Ordered         的 BeanDefinitionRegistryPostProcessor 的 postProcessBeanDefinitionRegistry 方法
				*         3. 执行了啥都没实现               的 BeanDefinitionRegistryPostProcessor 的 postProcessBeanDefinitionRegistry 方法
				*         4. 执行了以上这些                    BeanDefinitionRegistryPostProcessor的 postProcessBeanFactory 方法
				* 
				*  step2: 执行所有的 BeanFactoryPostProcessor
				*         1. 执行所有实现了 PriorityOrdered 的 BeanFactoryPostProcessor 的 postProcessBeanFactory 方法
				*         2. 执行所有实现了 Ordered         的 BeanFactoryPostProcessor 的 postProcessBeanFactory 方法
				*         3. 执行了啥都没实现               的 BeanFactoryPostProcessor 的 postProcessBeanFactory 方法
				* 
				*/
				// Invoke factory processors registered as beans in the context.
				invokeBeanFactoryPostProcessors(beanFactory);    // 见代码25

 

               // 重要：从容器中获取所有 BeanPostProcessors,并注册
			   // 注意，有一些 BeanPostProcessors，此时以普通bean的方式存在，当前方法根据类型获取所有 BeanPostProcessors 并注册
			   /*
 
				* 
				*/
				// Register bean processors that intercept bean creation.
				registerBeanPostProcessors(beanFactory);         // 见代码27



				beanPostProcess.end();



				// Initialize message source for this context.
				initMessageSource();

				// Initialize event multicaster for this context.
				initApplicationEventMulticaster();

				/*
				 *   由具体的容器来实现
				 *   对于AnnotationConfigServletWebServerApplicationContext，该方法会创建 WebServer
				 */
				// Initialize other special beans in specific context subclasses.
				onRefresh();               // 见 AnnotationConfigServletWebServerApplicationContext 父类 ServletWebServerApplicationContext 代码6

				// Check for listener beans and register them.
				registerListeners();

				// 实例化所有非延时加载的单例
				// Instantiate all remaining (non-lazy-init) singletons.
				finishBeanFactoryInitialization(beanFactory);

				// Last step: publish corresponding event.
				finishRefresh();
			}

			catch (BeansException ex) {
				if (logger.isWarnEnabled()) {
					logger.warn("Exception encountered during context initialization - " +
							"cancelling refresh attempt: " + ex);
				}

				// Destroy already created singletons to avoid dangling resources.
				destroyBeans();

				// Reset 'active' flag.
				cancelRefresh(ex);

				// Propagate exception to caller.
				throw ex;
			}

			finally {
				// Reset common introspection caches in Spring's core, since we
				// might not ever need metadata for singleton beans anymore...
				resetCommonCaches();
				contextRefresh.end();
			}
		}
	}



	/**
	 *   Prepare this context for refreshing, 
     *   setting its startup date and active flag as well as performing any initialization of property sources.
	 */
    // 代码11
	protected void prepareRefresh() {
		// Switch to active.
		this.startupDate = System.currentTimeMillis();
		this.closed.set(false);
		this.active.set(true);

		if (logger.isDebugEnabled()) {
			if (logger.isTraceEnabled()) {
				logger.trace("Refreshing " + this);
			}
			else {
				logger.debug("Refreshing " + getDisplayName());
			}
		}

		// 当前类 initPropertySources() 为空方法，由子类实现
		// Initialize any placeholder property sources in the context environment.
		initPropertySources();

		// Validate that all properties marked as required are resolvable:
		// see ConfigurablePropertyResolver#setRequiredProperties
		getEnvironment().validateRequiredProperties();

		// Store pre-refresh ApplicationListeners...
		if (this.earlyApplicationListeners == null) {
			this.earlyApplicationListeners = new LinkedHashSet<>(this.applicationListeners);
		}
		else {
			// Reset local application listeners to pre-refresh state.
			this.applicationListeners.clear();
			this.applicationListeners.addAll(this.earlyApplicationListeners);
		}

		// Allow for the collection of early ApplicationEvents,
		// to be published once the multicaster is available...
		this.earlyApplicationEvents = new LinkedHashSet<>();
	}


